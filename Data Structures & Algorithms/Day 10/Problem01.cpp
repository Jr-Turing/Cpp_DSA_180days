/*
    Problem: Arithmetic Multiplication
*/

#include<iostream>  
using namespace std;  

int main()
{
    // First way: Multiply float (13.2) with integer (4)
    // Result will be a floating-point value (52.8)
    cout<< 13.2 * 4 << endl;
    
    // Second way: Multiply integer (4) with float (13.2)
    // The order doesn't matter for multiplication; result is still 52.8
    cout<< 4 * 13.2;

    return 0;  
};
    
    